def test_postprocess_separates_input_roles_from_output_layout(tmp_path):
    subtitle = tmp_path / "bilingual.srt"
    subtitle.write_text(
        "1\n00:00:00,000 --> 00:00:02,000\n中文译文。\nEnglish original.\n",
        encoding="utf-8",
    )
    output = tmp_path / "postprocessed.srt"
    profile_path = repr(str(tmp_path / "profiles.json"))
    subtitle_path = repr(str(subtitle))
    output_path = repr(str(output))
    _run_qt_script(
        f"""
from pathlib import Path
from PyQt5.QtWidgets import QApplication
from videocaptioner.core.postprocess import PostprocessProfileStore
from videocaptioner.ui.view.postprocess_interface import PostprocessInterface

app = QApplication([])
widget = PostprocessInterface(profile_store=PostprocessProfileStore({profile_path}))
widget.load_subtitle({subtitle_path})
assert widget._structure_confirmation_required

# 输入第一行是译文；表格字段应恢复为英文原文、中文译文。
widget.layout_combo.setCurrentIndex(widget.layout_combo.findData('translate_on_top'))
app.processEvents()
assert widget.model._data['1']['original_subtitle'] == 'English original.'
assert widget.model._data['1']['translated_subtitle'] == '中文译文。'

# 输出布局是独立选择，能够真正交换可见上下行。
widget.output_layout_combo.setCurrentIndex(
    widget.output_layout_combo.findData('原文在上')
)
widget.primary_srt_path = {output_path}
assert widget.save_primary_srt(show_feedback=False)
lines = Path({output_path}).read_text(encoding='utf-8').splitlines()
assert lines[2] == 'English original.'
assert lines[3] == '中文译文。'
widget.close()
"""
    )


